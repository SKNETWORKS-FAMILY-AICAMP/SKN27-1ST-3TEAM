use car_insert;
# 연도별 전체 자동차 등록 대수
CREATE TABLE total_vehicle_yearly (
    vehicle_id    INT NOT NULL AUTO_INCREMENT,
    reg_year      INT NOT NULL,
    total_vehicle INT NOT NULL,
    PRIMARY KEY (vehicle_id),
    CONSTRAINT fk_vehicle_year FOREIGN KEY (reg_year) REFERENCES base_year (reg_year)
);
# 연도별 전국 수소차 누적 등록 대수
CREATE TABLE hydrogen_yearly (
    h2_yearly_id INT NOT NULL AUTO_INCREMENT,
    reg_year     INT NOT NULL,
    h2_count     INT NOT NULL,
    PRIMARY KEY (h2_yearly_id),
    CONSTRAINT fk_h2_year FOREIGN KEY (reg_year) REFERENCES base_year (reg_year)
);
# 연도별 권역별 전기차 충전소 수
CREATE TABLE charger_yearly (
    charger_id      INT NOT NULL AUTO_INCREMENT,
    reg_year        INT NOT NULL,
    seoul_cnt       INT NOT NULL DEFAULT 0,
    gyeonggi_cnt    INT NOT NULL DEFAULT 0,
    incheon_cnt     INT NOT NULL DEFAULT 0,
    gangwon_cnt     INT NOT NULL DEFAULT 0,
    chungcheong_cnt INT NOT NULL DEFAULT 0,
    jeolla_cnt      INT NOT NULL DEFAULT 0,
    gyeongsang_cnt  INT NOT NULL DEFAULT 0,
    jeju_cnt        INT NOT NULL DEFAULT 0,
    total_cnt       INT NOT NULL DEFAULT 0,
    PRIMARY KEY (charger_id),
    CONSTRAINT fk_charger_year FOREIGN KEY (reg_year) REFERENCES base_year (reg_year)
);
# 시도별 연도별 전기차 등록대수, 핵심 중심 테이블
CREATE TABLE ev_registration (
    ev_reg_id INT NOT NULL AUTO_INCREMENT,
    region_id INT NOT NULL,
    reg_year  INT NOT NULL,
    ev_count  INT NOT NULL,
    PRIMARY KEY (ev_reg_id),
    CONSTRAINT fk_ev_region FOREIGN KEY (region_id) REFERENCES region (region_id),
    CONSTRAINT fk_ev_year   FOREIGN KEY (reg_year)  REFERENCES base_year (reg_year)
);
# 시도별 연도별 수송 CO2 배출량
CREATE TABLE transport_co2 (
    co2_id    INT         NOT NULL AUTO_INCREMENT,
    region_id INT         NOT NULL,
    reg_year  INT         NOT NULL,
    criteria  VARCHAR(10) NOT NULL,
    emission  FLOAT       NOT NULL,
    PRIMARY KEY (co2_id),
    CONSTRAINT fk_co2_region FOREIGN KEY (region_id) REFERENCES region (region_id),
    CONSTRAINT fk_co2_year   FOREIGN KEY (reg_year)  REFERENCES base_year (reg_year)
);
# 2025년 12월 기준 시도별 수소차 등록대수
CREATE TABLE hydrogen_regional (
    h2_reg_id INT         NOT NULL AUTO_INCREMENT,
    region_id INT         NOT NULL,
    h2_count  INT         NOT NULL,
    base_ym   VARCHAR(10) NOT NULL,
    PRIMARY KEY (h2_reg_id),
    CONSTRAINT fk_h2reg_region FOREIGN KEY (region_id) REFERENCES region (region_id)
);
# 2025년 기준 시도별 인구수
CREATE TABLE population (
    pop_id     INT NOT NULL AUTO_INCREMENT,
    region_id  INT NOT NULL,
    population INT NOT NULL,
    PRIMARY KEY (pop_id),
    CONSTRAINT fk_pop_region FOREIGN KEY (region_id) REFERENCES region (region_id)
);
#2026 지자체별 전기차 + 수소차 보조금
CREATE TABLE subsidy (
    subsidy_id     INT         NOT NULL AUTO_INCREMENT,
    region_id      INT         NOT NULL,
    local_gov_name VARCHAR(30) NOT NULL,
    ev_sedan_amt   INT         NOT NULL DEFAULT 0,
    ev_small_amt   INT         NOT NULL DEFAULT 0,
    ev_mid_amt     INT         NOT NULL DEFAULT 0,
    ev_large_amt   INT         NOT NULL DEFAULT 0,
    h2_sedan_amt   INT         NOT NULL DEFAULT 0,
    h2_van_amt     INT         NOT NULL DEFAULT 0,
    PRIMARY KEY (subsidy_id),
    CONSTRAINT fk_subsidy_region FOREIGN KEY (region_id) REFERENCES region (region_id)
);

