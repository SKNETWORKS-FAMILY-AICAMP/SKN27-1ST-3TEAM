USE car_insert;
# 17시도 테이블
CREATE TABLE region (
    region_id   INT         NOT NULL AUTO_INCREMENT,
    region_name VARCHAR(20) NOT NULL,
    PRIMARY KEY (region_id)
);
# 2010 ~ 2015년까지 나타낸 테이블
CREATE TABLE base_year (
    reg_year INT NOT NULL,
    PRIMARY KEY (reg_year)
);
# 연도별 전국 평균기온
CREATE TABLE temperature_yearly (
    temp_id  INT   NOT NULL AUTO_INCREMENT,
    reg_year INT   NOT NULL,
    avg_temp FLOAT NOT NULL,
    PRIMARY KEY (temp_id),
    CONSTRAINT fk_temp_year FOREIGN KEY (reg_year) REFERENCES base_year (reg_year)
);
